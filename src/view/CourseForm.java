private CourseController controller = new CourseController();
private DefaultTableModel tableModel;
private int selectedId = -1;
private int currentPage = 1;
private int totalPages = 1;
private final int PAGE_SIZE = 5;
private String searchKeyword = "";

private void setupSemesterCombo() {
    semesterCombo.removeAllItems();
    for (int i = 1; i <= 10; i++) {
        semesterCombo.addItem(String.valueOf(i));
    }
}

private void setupTable() {
    tableModel = new DefaultTableModel(new Object[]{"ID", "Kode", "Nama", "SKS", "Semester"}, 0) {
        @Override
        public boolean isCellEditable(int row, int column) { return false; }
    };
    table.setModel(tableModel);

    table.getSelectionModel().addListSelectionListener(e -> {
        int row = table.getSelectedRow();
        if (row >= 0) {
            selectedId = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
            kodeField.setText(tableModel.getValueAt(row, 1).toString());
            namaField.setText(tableModel.getValueAt(row, 2).toString());
            sksField.setText(tableModel.getValueAt(row, 3).toString());
            semesterCombo.setSelectedItem(tableModel.getValueAt(row, 4).toString());        }
    });
}
private void loadData() {
    int total;
    List<Course> list;
    int offset = (currentPage - 1) * PAGE_SIZE;

    if (searchKeyword.isEmpty()) {
        total = controller.getTotalCount();
        list = controller.getCoursePage(offset, PAGE_SIZE);
    } else {
        total = controller.getSearchCount(searchKeyword);
        list = controller.searchByCode(searchKeyword, offset, PAGE_SIZE);
    }

    totalPages = (int) Math.ceil((double) total / PAGE_SIZE);
    if (totalPages == 0) totalPages = 1;
    if (currentPage > totalPages) currentPage = totalPages;

    tableModel.setRowCount(0);
    for (Course c : list) {
        tableModel.addRow(new Object[]{c.getId(), c.getCode(), c.getCourseName(), c.getSKS(), c.getSemester()});
    }
    lblPage.setText("Halaman " + currentPage + " dari " + totalPages);
}
private void clearForm() {
    kodeField.setText(""); namaField.setText(""); sksField.setText("");
    semesterCombo.setSelectedIndex(0); // tambahin ini
    selectedId = -1;
    table.clearSelection();
}

private boolean isFormEmpty() {
    if (kodeField.getText().isEmpty() || namaField.getText().isEmpty() || sksField.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return true;
    }
    return false;
}
    
    

