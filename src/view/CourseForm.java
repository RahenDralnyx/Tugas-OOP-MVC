    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         if (isFormEmpty()) return;
    int sks;
    try {
        sks = Integer.parseInt(sksField.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "SKS harus angka.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    int semester = Integer.parseInt((String) semesterCombo.getSelectedItem());

    int result;
    if (selectedId == -1) {
        Course c = new Course(kodeField.getText(), namaField.getText(), sks, semester);
        result = controller.create(c);
    } else {
        Course c = new Course(selectedId, kodeField.getText(), namaField.getText(), sks, semester);
        result = controller.update(c);
    }

    if (result == 1) {
        JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil disimpan.");
        loadData();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
  if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dari tabel dulu.");
        return;
    }
    int confirm = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        if (controller.delete(selectedId) == 1) {
            JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil dihapus.");
            loadData();
            clearForm();
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

