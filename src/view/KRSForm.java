private KRSController krsController = new KRSController();

private List<Student> studentList = new ArrayList<>();
private List<Course> courseList = new ArrayList<>();
private List<Lecturer> lecturerList = new ArrayList<>();
private DefaultTableModel tableModel;
private int selectedId = -1;
private int currentPage = 1;
private int totalPages = 1;
private final int PAGE_SIZE = 5;
private String searchKeyword = "";

private void setupCombos() {
    studentList = studentController.getStudent();
    mahasiswaCombo.removeAllItems();
    for (Student s : studentList) mahasiswaCombo.addItem(s.getName());

    courseList = courseController.getCourse();
    mkCombo.removeAllItems();
    for (Course c : courseList) mkCombo.addItem(c.getCourseName());

    lecturerList = lecturerController.getLecturer();
    dosenCombo.removeAllItems();
    for (Lecturer l : lecturerList) dosenCombo.addItem(l.getName());

    semesterCombo.removeAllItems();
    for (int i = 1; i <= 10; i++) semesterCombo.addItem(String.valueOf(i));
}

private void updateMahasiswaLabel() {
    int idx = mahasiswaCombo.getSelectedIndex();
    if (idx >= 0 && idx < studentList.size()) {
        Student s = studentList.get(idx);
        labelNim.setText(s.getNim() + " | " + s.getStudyProgram());
    }
}

private void updateMkLabel() {
    int idx = mkCombo.getSelectedIndex();
    if (idx >= 0 && idx < courseList.size()) {
        Course c = courseList.get(idx);
        labelMkSks.setText(c.getCode() + " (" + c.getSKS() + " SKS)");
    }
}

private void updateDosenLabel() {
    int idx = dosenCombo.getSelectedIndex();
    if (idx >= 0 && idx < lecturerList.size()) {
        Lecturer l = lecturerList.get(idx);
        labelNidn.setText(l.getNidn() + " | Pengampu: " + l.getExpertise());
    }
}
private void setupTable() {
    tableModel = new DefaultTableModel(
        new Object[]{"ID", "NIM", "Nama", "Matakuliah", "SKS", "Semester", "Nilai", "Dosen"}, 0) {
        @Override
        public boolean isCellEditable(int row, int column) { return false; }
    };
    table.setModel(tableModel);

    table.getSelectionModel().addListSelectionListener(e -> {
        int row = table.getSelectedRow();
        if (row >= 0) {
            selectedId = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
            String nim = tableModel.getValueAt(row, 1).toString();
            String courseName = tableModel.getValueAt(row, 3).toString();
            String semester = tableModel.getValueAt(row, 5).toString();
            String dosenName = tableModel.getValueAt(row, 7).toString();

            selectByNim(nim);
            selectByCourseName(courseName);
            selectByDosenName(dosenName);
            semesterCombo.setSelectedItem(semester);
        }
    });
}

private void selectByNim(String nim) {
    for (int i = 0; i < studentList.size(); i++) {
        if (studentList.get(i).getNim().equals(nim)) { mahasiswaCombo.setSelectedIndex(i); break; }
    }
}

private void selectByCourseName(String name) {
    for (int i = 0; i < courseList.size(); i++) {
        if (courseList.get(i).getCourseName().equals(name)) { mkCombo.setSelectedIndex(i); break; }
    }
}

private void selectByDosenName(String name) {
    for (int i = 0; i < lecturerList.size(); i++) {
        if (lecturerList.get(i).getName().equals(name)) { dosenCombo.setSelectedIndex(i); break; }
    }
}

private void loadData() {
    int total;
    List<Object[]> list;
    int offset = (currentPage - 1) * PAGE_SIZE;

    if (searchKeyword.isEmpty()) {
        total = krsController.getTotalCount();
        list = krsController.getKRSPage(offset, PAGE_SIZE);
    } else {
        total = krsController.getSearchCount(searchKeyword);
        list = krsController.searchByNim(searchKeyword, offset, PAGE_SIZE);
    }

    totalPages = (int) Math.ceil((double) total / PAGE_SIZE);
    if (totalPages == 0) totalPages = 1;
    if (currentPage > totalPages) currentPage = totalPages;

    tableModel.setRowCount(0);
    for (Object[] row : list) tableModel.addRow(row);
    lblPage.setText("Halaman " + currentPage + " dari " + totalPages);
}
private void clearForm() {
    if (!studentList.isEmpty()) mahasiswaCombo.setSelectedIndex(0);
    if (!courseList.isEmpty()) mkCombo.setSelectedIndex(0);
    if (!lecturerList.isEmpty()) dosenCombo.setSelectedIndex(0);
    semesterCombo.setSelectedIndex(0);
    sikapField.setText("0");
    utsField.setText("0");
    uasField.setText("0");
    selectedId = -1;
    table.clearSelection();
    updateMahasiswaLabel();
    updateMkLabel();
    updateDosenLabel();
}

