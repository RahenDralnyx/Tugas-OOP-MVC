    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dari tabel dulu.");
        return;
    }
    int confirm = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        if (krsController.delete(selectedId) == 1) {
            JOptionPane.showMessageDialog(this, "Data KRS berhasil dihapus.");
            loadData();
            clearForm();
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    if (studentList.isEmpty() || courseList.isEmpty() || lecturerList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data mahasiswa/matakuliah/dosen belum tersedia.");
        return;
    }
    int sikap, uts, uas;
    try {
        sikap = Integer.parseInt(sikapField.getText());
        uts = Integer.parseInt(utsField.getText());
        uas = Integer.parseInt(uasField.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Nilai harus berupa angka.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    Student student = studentList.get(mahasiswaCombo.getSelectedIndex());
    Course course = courseList.get(mkCombo.getSelectedIndex());
    Lecturer lecturer = lecturerList.get(dosenCombo.getSelectedIndex());
    int semester = Integer.parseInt((String) semesterCombo.getSelectedItem());

    double score = (sikap + uts + uas) / 3.0;
    KRS tempKrs = new KRS(course, score);
    String grade = tempKrs.getGrade();

    int result;
    if (selectedId == -1) {
        result = krsController.create(student.getNim(), course.getId(), lecturer.getId(),
                semester, sikap, uts, uas, score, grade);
    } else {
        result = krsController.update(selectedId, student.getNim(), course.getId(), lecturer.getId(),
                semester, sikap, uts, uas, score, grade);
    }

    if (result == 1) {
        JOptionPane.showMessageDialog(this, "Data KRS berhasil disimpan.");
        loadData();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

